#include <stdio.h>

int main() {
    int a, b, c;
    
    printf("Адрес a: %p\n", (void*)&a);
    printf("Адрес b: %p\n", (void*)&b);
    printf("Адрес c: %p\n", (void*)&c);
    
    // Вычислила разницу между адресами (в байтах)
    printf("Разница между a и b: %ld байт\n", 
           (long)&b - (long)&a);
    printf("Разница между b и c: %ld байт\n", 
           (long)&c - (long)&b);
    
    printf("Объяснение: переменные расположены в стеке последовательно, ");
    printf("но между ними может быть пустое место (выравнивание) ");
    printf("или компилятор может расположить их не подряд\n");
    
    return 0;
}

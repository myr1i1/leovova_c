#include <stdio.h>
#include <limits.h>

int main() {
    int x = INT_MAX;
    printf("Максимальное значение int: %d\n", x);
    
    x = x + 1;
    printf("После прибавления 1: %d\n", x);
    printf("Объяснение: произошло переполнение - число вышло за пределы диапазона int\n");
    printf("Результат стал отрицательным из-за особенностей двоичного представления\n");
    
    return 0;
}
